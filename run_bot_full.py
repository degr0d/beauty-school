    logger.info(f"‚úÖ –ë–æ—Ç –∑–∞–ø—É—â–µ–Ω: @{me.username}")
    logger.info(f"   ID: {me.id}")
    logger.info(f"   –ò–º—è: {me.first_name}")
    
    logger.info("=" * 60)
    logger.info("üöÄ –ë–æ—Ç –≥–æ—Ç–æ–≤ –∫ —Ä–∞–±–æ—Ç–µ!")
