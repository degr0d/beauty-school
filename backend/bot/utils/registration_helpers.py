def validate_fullname(fullname: str) -> Tuple[bool, Optional[str]]:
    """
    –í–∞–ª–∏–¥–∏—Ä—É–µ—Ç –§–ò–û
    
    Returns:
        (is_valid, error_message)
    """
    fullname = fullname.strip()
    if len(fullname.split()) < 2:
        return False, (
            "‚ùå –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏ –§–ò–û –ø–æ–ª–Ω–æ—Å—Ç—å—é (–º–∏–Ω–∏–º—É–º –ò–º—è –∏ –§–∞–º–∏–ª–∏—è)\n\n"
            "<i>–ù–∞–ø—Ä–∏–º–µ—Ä: –ò–≤–∞–Ω–æ–≤–∞ –ú–∞—Ä–∏—è</i>"
        )
    return True, None


def validate_phone(phone: str) -> Tuple[bool, Optional[str]]:
    """
    –í–∞–ª–∏–¥–∏—Ä—É–µ—Ç —Ç–µ–ª–µ—Ñ–æ–Ω
    
    Returns:
        (is_valid, error_message)
    """
    phone = phone.strip()
    # –£–¥–∞–ª—è–µ–º –≤—Å–µ –Ω–µ—Ü–∏—Ñ—Ä–æ–≤—ã–µ —Å–∏–º–≤–æ–ª—ã –∫—Ä–æ–º–µ +
    cleaned = phone.replace("+", "").replace("-", "").replace("(", "").replace(")", "").replace(" ", "")
    
    if not cleaned.isdigit():
        return False, (
            "‚ùå –ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç —Ç–µ–ª–µ—Ñ–æ–Ω–∞\n\n"
            "–ü–æ–ø—Ä–æ–±—É–π –µ—â—ë —Ä–∞–∑ –∏–ª–∏ –Ω–∞–∂–º–∏ –∫–Ω–æ–ø–∫—É ¬´–ü–æ–¥–µ–ª–∏—Ç—å—Å—è –∫–æ–Ω—Ç–∞–∫—Ç–æ–º¬ª"
        )
    return True, None

