    echo -e "${RED}‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ push${NC}"
    exit 1
}

echo ""
echo -e "${GREEN}‚úÖ –ò–∑–º–µ–Ω–µ–Ω–∏—è –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω—ã –≤ GitHub!${NC}"
echo ""
echo -e "${GREEN}üöÄ –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –¥–µ–ø–ª–æ–π –∑–∞–ø—É—â–µ–Ω:${NC}"
echo "  - Railway (Backend) –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–¥–µ–ø–ª–æ–∏—Ç –ø—Ä–∏ push –≤ main"
echo "  - Vercel (Frontend) –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–¥–µ–ø–ª–æ–∏—Ç –ø—Ä–∏ push –≤ main"
echo ""
echo -e "${YELLOW}‚è≥ –û–∂–∏–¥–∞–π—Ç–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –¥–µ–ø–ª–æ—è (–æ–±—ã—á–Ω–æ 2-5 –º–∏–Ω—É—Ç)${NC}"
echo ""
echo "–ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å—Ç–∞—Ç—É—Å:"
echo "  - Railway: https://railway.app"
echo "  - Vercel: https://vercel.com"

